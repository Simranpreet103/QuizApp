    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       try{
                
           int t=0;
        
            if(REGISTRATIONID==0)
            {
                
                 PreparedStatement ps=Quizapp.con.prepareStatement("insert into quiz values(?,?,?,?,?,?)");
               
               ps.setString(1, jTextField1.getText());
               if(jRadioButton1.isSelected())
                   ps.setString(2,"Male");
               else if(jRadioButton2.isSelected())
                   ps.setString(2, "Female");
               ps.setString(3,jTextField3.getText());
              if(jPasswordField1.getText().equals(jPasswordField2.getText()))
                    {
                        ps.setString(4,jPasswordField1.getText());
                        // ps.setString(5,jPasswordField2.getText());
                    }
              else
              {
                  JOptionPane.showMessageDialog(rootPane,"password does not match");
                  ps.setString(4, null);
                  t++;
                   
              }
               int i=jComboBox1.getSelectedIndex();
               String s= jComboBox1.getItemAt(i).toString();
               ps.setString(5, s);
               ps.setString(6,jTextField6.getText());
               if(validdata()==true && t==0)
               {
               ps.execute();
               JOptionPane.showMessageDialog(rootPane,"Registration Succesful...");
               jTextField1.setText("");
               jTextField3.setText("");
               jPasswordField1.setText("");
               jPasswordField2.setText("");
               jTextField6.setText("");
               this.dispose();
               }
               else
               {
                   Statement st = Quizapp.con.createStatement();
                   st.execute("Delete from quiz where REGISTRATIONID="+Quizapp.RegId);
               }
            
               
               }
        
        
        }//try
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(rootPane, ex);
        }//catch
       
    }//GEN-LAST:event_jButton2ActionPerformed

